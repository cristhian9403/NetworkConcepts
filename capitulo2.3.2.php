							 <h3 align="center"> <font color="#000080" ><b>2.3.2 Dispositivos Intermediarios </b></h3></font>
							 <br>
								Además de los dispositivos finales con los cuales la gente está familiarizada, las redes dependen de dispositivos
								intermediarios para proporcionar conectividad y para trabajar detrás de escena y garantizar que los datos fluyan 
								a través de la red. Estos dispositivos conectan los hosts individuales a la red y pueden conectar varias redes 
								individuales para formar una internetwork. Los siguientes son ejemplos de dispositivos de red intermediarios:

							<br>
							<br>
							<ul type="disk">
								<li> Dispositivos de acceso a la red (hubs, switches y puntos de acceso inalámbricos),</li>
								<li> Dispositivos de internetworking (routers),</li>
								<li> Servidores de comunicación y módems, y</li>
								<li> Dispositivos de seguridad (firewalls).</li>
							</ul>
							
							<br>
                                La administración de datos mientras fluyen a través de la red también es una función de los dispositivos intermediarios.
								Estos dispositivos utilizan la dirección host de destino, conjuntamente con información sobre las interconexiones de la 
								red, para determinar la ruta que deben tomar los mensajes a través de la red. Los procesos que se ejecutan en los 
								dispositivos de red intermediarios realizan las siguientes funciones: 
							<br>
							<br>
							<ul type="disk">
								<li>Regenerar y retransmitr señales de datos,</li>
								<li>Mantener información sobre qué rutas existen a través de la red y de la internetwork,</li>
								<li>Notificar a otros dispositivos los errores y las fallas de comunicación,</li>
								<li>Direccionar datos por rutas alternativas cuando existen fallas en un enlace,</li>
								<li>Clasificar y direccionar mensajes según las prioridades de QoS (calidad de servicio), y</li>
								<li>Permitir o denegar el flujo de datos en base a configuraciones de seguridad. </li>
							</ul>
								
