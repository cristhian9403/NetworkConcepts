							 <h3 align="center"> <font color="#000080" ><b> 2.3.1 Dispositivos Finales </b></h3></font>
							 <br>
								Los dispositivos de red con los que la gente está más familiarizada se denominan dispositivos finales.
								Estos dispositivos constituyen la interfaz entre la red humana y la red de comunicación subyacente. 
								Algunos ejemplos de dispositivos finales son:

							<br>
							<br>
							<ul type="disk">
								<li> Computadoras (estaciones de trabajo, computadoras portátiles, servidores de archivos, servidores Web).</li>
								<li> Impresoras de red.</li>
								<li> Teléfonos VoIP.</li>
								<li> Cámaras de seguridad.</li>
								<li> Dispositivos móviles de mano (como escáneres de barras inalámbricos, asistentes digitales personales (PDA)).</li>
							</ul>
							
							<br>
                                En el contexto de una red, los dispositivos finales se denominan host. Un dispositivo host puede ser el origen o 
								el destino de un mensaje transmitido a través de la red. Para distinguir un host de otro, cada host en la red se 
								identifica por una dirección. Cuando un host inicia una comunicación, utiliza la dirección del host de destino 
								para especificar dónde debe ser enviado el mensaje.
							<br>
							<br>
							    En las redes modernas, un host puede funcionar como un cliente, como un servidor o como ambos. El software instalado 
								en el host determina qué rol representa en la red.
							<br>
							<br>
								Los servidores son hosts que tienen software instalado que les permite proporcionar información y servicios, como
								e-mail o páginas Web, a otros hosts en la red.
							<br>
							<br>
								Los clientes son hosts que tienen software instalado que les permite solicitar y mostrar la información obtenida 
								del servidor. 
								
